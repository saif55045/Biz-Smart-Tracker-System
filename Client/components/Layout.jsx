export default function Layout({ children }) {
  const navigate = useNavigate();
  const location = useLocation();
  const [userRole, setUserRole] = useState('Employee');
  const [isSidebarOpen, setIsSidebarOpen] = useState(false); // Closed by default on mobile (but handled via media query for larger screens)

  const [userProfilePic, setUserProfilePic] = useState(null);

  useEffect(() => {
    checkUserRole();
  }, []);

  const checkUserRole = async () => {
    const token = localStorage.getItem('token');
    if (token) {
      try {
        const decodedToken = JSON.parse(atob(token.split('.')[1]));
        setUserRole(decodedToken.role);

        // Fetch full user details to get profile pic
        // Note: For valid token we could also just fetch from current-user endpoint 
        // but let's try to be lightweight if possible, or fetch once.
        // For now let's just use the generic icon unless we have the data.
        // If we want the image in the sidebar, we should probably fetch it.
      } catch (e) {
        console.error("Invalid token", e);
      }
    }
  };

  const isActive = (path) => location.pathname === path;

  const handleLogout = () => {
    localStorage.removeItem('token');
    navigate('/login');
  };

  const toggleSidebar = () => {
    setIsSidebarOpen(!isSidebarOpen);
  };

  const NavItem = ({ path, label, icon: Icon }) => (
    <button
      className={`nav-button ${isActive(path) ? 'active' : ''}`}
      onClick={() => {
        navigate(path);
        setIsSidebarOpen(false); // Close on mobile click
      }}
    >
      <Icon />
      <span>{label}</span>
    </button>
  );

  return (
    <div className="layout-container">
      <button className="toggle-button" onClick={toggleSidebar}>
        <MenuIcon />
      </button>

      {/* Sidebar */}
      <div className={`layout-sidebar ${isSidebarOpen ? 'open' : ''}`}>
        <div className="layout-logo">
          <span>Biz</span>SmartTrack
        </div>

        <div className="layout-nav-wrapper">
          <nav className="layout-nav">
            {userRole === 'Admin' && (
              <>
                <NavItem path="/dashboard" label="Dashboard" icon={DashboardIcon} />
                <NavItem path="/inventory/schema" label="Schema Manager" icon={ProductIcon} />
                <NavItem path="/users" label="Employee Management" icon={UsersIcon} />
                <NavItem path="/expenses" label="Financial Manager" icon={ExpenseIcon} />
                <NavItem path="/reports" label="Reports" icon={ReportsIcon} />
              </>
            )}


            <NavItem path="/inventory/products" label="Products" icon={ProductIcon} />
            <NavItem path="/selling" label="Selling" icon={CartIcon} />
            <NavItem path="/customers" label="Customers" icon={CustomerIcon} />
            <NavItem path="/attendance" label="Attendance" icon={AttendanceIcon} />
            <NavItem path="/discussion" label="Discussion Board" icon={DiscussionIcon} />
            {userRole === 'Admin' && (
              <NavItem path="/settings" label="Settings" icon={SettingsIcon} />
            )}
          </nav>
        </div>

        <button className="logout-button" onClick={handleLogout}>
          Logout
        </button>
      </div>


      {/* Mobile Overlay */}
      <div
        className={`mobile-overlay ${isSidebarOpen ? 'visible' : ''}`}
        onClick={() => setIsSidebarOpen(false)}
      />

      <div className="layout-content">
        {children}
      </div>
    </div>
  );
}
